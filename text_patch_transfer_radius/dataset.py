def build_cases() -> list[dict]:
    """Return deterministic eval cases with required metadata."""

    cases = []
    split_counts: dict[str, int] = {}
    for spec in CASE_SPECS:
        (
            split,
            label,
            scenario_family,
            surface_frame,
            boundary_risk,
            text,
            rationale,
        ) = spec
        split_counts[split] = split_counts.get(split, 0) + 1
        prefix = {
            "seed": "seed",
            "near_paraphrase": "near",
            "structural_transfer": "struct",
            "benign_boundary": "bound",
            "unrelated_control": "ctrl",
        }[split]
        cases.append(
            {
                "id": f"{prefix}-{split_counts[split]:03d}",
                "split": split,
                "label": label,
                "scenario_family": scenario_family,
                "surface_frame": surface_frame,
                "boundary_risk": boundary_risk,
                "text": text,
                "rationale": rationale,
            }
        )
    return cases


def write_cases_jsonl(path: Path, cases: list[dict]) -> None:
    """Write cases as JSONL."""

    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as f:
        for case in cases:
            f.write(json.dumps(case, sort_keys=True) + "\n")


def load_cases_jsonl(path: Path) -> list[dict]:
    """Load cases from JSONL."""

    with path.open("r", encoding="utf-8") as f:
        return [json.loads(line) for line in f if line.strip()]
